function isHoliday(d: Date): boolean {
  const str = d.toISOString().split("T")[0];
  return JP_HOLIDAYS.has(str);
}

function isWeekend(d: Date): boolean {
  const day = d.getDay();
  return day === 0 || day === 6; // 0=日, 6=土
}

/** N営業日後（土日祝スキップ）の日付を返す */
export function addBusinessDays(days: number): string {
  const d = new Date();
  let added = 0;
  while (added < days) {
    d.setDate(d.getDate() + 1);
    if (!isWeekend(d) && !isHoliday(d)) added++;
  }
  return d.toISOString().split("T")[0];
}
